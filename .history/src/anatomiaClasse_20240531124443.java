public class anatomiaClasse {
    public static void main (String[] args){
        System.out.println("Olá Mundo");
    }
}

/*
Declaração da Classe

public: Define que a classe é acessível de qualquer lugar no seu projeto.
class: Indica o início da definição de uma classe.
anatomiaClasse: Nome da classe. Escolha um nome descritivo e significativo que represente o propósito da classe.

Metodo Main

public: Define que o método main é acessível de qualquer lugar no seu projeto.
static: Indica que o método main pertence à classe em si, não a um objeto específico da classe.
void: Especifica o tipo de retorno do método, neste caso, nenhum valor é retornado.
main: Nome do método que serve como ponto de entrada para a execução do programa.
String[] args: Parâmetros do método main. O array args contém os argumentos passados para o programa quando ele é executado.

 */